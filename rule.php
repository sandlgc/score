    <h1>积分调整规则</h1>
    <h2>1. 基础积分</h2>
    <p>每次答题后会根据您的答题情况获得原始积分。原始积分是后续积分调整的基础。</p>
    <h2>2. 阈值调整</h2>
    <h3>2.1 章节难度影响</h3>
    <p>系统会参考该章节所有学生的平均正确率来调整积分阈值：</p>
    <ul>
        <li>如果章节平均正确率大于 80%，阈值会提高到 0.9。例如，当章节平均正确率为 85% 时，阈值变为 0.9。</li>
        <li>如果章节平均正确率小于 50%，阈值会降低到 0.6。例如，当章节平均正确率为 40% 时，阈值变为 0.6。</li>
        <li>其他情况阈值默认为 0.7。</li>
    </ul>
    <h3>2.2 个人表现影响</h3>
    <p>当您在该章节的答题次数超过 2 次后，以下情况会影响阈值：</p>
    <ul>
        <li>若您的平均正确率比章节平均正确率高 10% 以上，阈值可能会提高。例如，章节平均正确率为 70%，您的平均正确率为 81%，阈值会相应提高。</li>
        <li>若您连续答对 5 题及以上，阈值可能会提高。</li>
        <li>若您的答题平均时间明显短于该章节平均答题时间（短于 60%），阈值可能会提高。</li>
        <li>阈值最高提高到 0.95。</li>
    </ul>
    <h2>3. 积分系数计算</h2>
    <h3>3.1 正确率调整</h3>
    <p>如果您的答题正确率超过动态阈值，会根据超出比例计算积分系数，积分系数最低不低于 0.3。例如，当动态阈值为 0.7，您的正确率为 0.8，会根据公式计算积分系数，超出比例越高，系数越小，积分可能会打折。</p>
    <h3>3.2 答题时间调整</h3>
    <p>若每题平均答题时间少于 3 秒，积分会额外打折，时间系数最低为 0.5。最终积分系数是正确率调整系数和时间系数的乘积，且不低于 0.3。例如，正确率调整系数为 0.8，时间系数为 0.5，最终积分系数为 0.4（但最低不低于 0.3）。</p>
    <h2>4. 调整后积分</h2>
    <p>原始积分乘以积分系数，得到调整后的积分，结果会保留两位小数。例如，原始积分为 2.5，积分系数为 0.8，调整后积分为 2.00。</p>
    <h2>5. 积分记录限制</h2>
    <ul>
        <li>每天在同一章节最多记录 3 次积分，超过 3 次将不再记录该章节积分。</li>
        <li>每天选择题答题获得的总积分上限是 8 分，超过 8 分后，后续答题不再记录积分，但仍可继续学习。</li>
    </ul>
